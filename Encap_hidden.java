// Encapsulation - Hidden/Private --> Get & Set
public class Encap_hidden {
    private int age;
    private String name;
    public int tilak1() { //Get
        return age;
    }
    public String tilak2() { //Get
        return name;
    }
    public void rohit(int x,String y) { //set
        age = x;
        name = y;
    }
}